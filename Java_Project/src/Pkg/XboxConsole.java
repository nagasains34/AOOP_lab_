package Pkg;

class XboxConsole {
    public void startConsole() {
        System.out.println("Xbox Console is turned ON");
    }

    public void shutdownConsole() {
        System.out.println("Xbox Console is turned OFF");
    }

    public void increaseVolume() {
        System.out.println("Xbox Console Volume Increased");
    }

    public void decreaseVolume() {
        System.out.println("Xbox Console Volume Decreased");
    }
}
