package Pkg;

class BoseSoundSystem {
    public void powerOnSystem() {
        System.out.println("Bose Sound System is turned ON");
    }

    public void powerOffSystem() {
        System.out.println("Bose Sound System is turned OFF");
    }

    public void raiseVolume() {
        System.out.println("Bose Sound System Volume Increased");
    }

    public void lowerVolume() {
        System.out.println("Bose Sound System Volume Decreased");
    }
}